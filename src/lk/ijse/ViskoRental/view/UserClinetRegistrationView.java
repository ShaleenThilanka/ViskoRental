    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        try {
            ArrayList<Client> allclient = ClientRegistrationcontrollr.getAllclient();
            DefaultTableModel dtm = (DefaultTableModel) tblClient.getModel();
            dtm.setRowCount(0);
            for (Client client : allclient) {
                Object[] rowData = {
                    client.getClientID(),
                    client.getUserID(),
                    client.getClientName(),
                    client.getClientContact(),
                    client.getClientNIC_No(),
                    client.getClientAddress()};
                dtm.addRow(rowData);

            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminClintRegistratoinView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AdminClintRegistratoinView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnRefreshActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Client client = new Client(
                txtID.getText(),
                cmbUserID.getSelectedItem().toString(),
                txtName.getText(),
                txtContact.getText(),
                txtNic.getText(),
                txtAddress.getText());
        try {
            boolean isUpdated = ClientRegistrationcontrollr.updateClient(client);
            if (isUpdated) {
                JOptionPane.showMessageDialog(this, "Updated...");
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Update fail....");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminClintRegistratoinView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AdminClintRegistratoinView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

