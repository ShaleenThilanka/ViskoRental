    private void addClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClientActionPerformed

        String id = txtID.getText();
        String userid = cmbUserID.getSelectedItem().toString();
        String name = txtName.getText();
        String contact = txtContact.getText();
        String nic = txtNic.getText();
        String address = txtAddress.getText();

        Client client = new Client(id, userid, name, contact, nic, address);
        if (!userid.isEmpty() && !id.isEmpty() && !name.isEmpty() && !contact.isEmpty() && !nic.isEmpty() && !address.isEmpty()) {
            try {
                boolean isAdded = ClientRegistrationcontrollr.AddClient(client);
                if (isAdded) {

                    JOptionPane.showMessageDialog(this, "Added Success");
                    clear();
                    generateClientID();
                    loadAllClient();

                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminClintRegistratoinView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } catch (SQLException ex) {
                Logger.getLogger(AdminClintRegistratoinView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Try Again? Complete All action",
                    "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_addClientActionPerformed

