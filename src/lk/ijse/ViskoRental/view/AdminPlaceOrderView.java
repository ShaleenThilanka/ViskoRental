    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbIItemDetails.getModel();
        int qty = Integer.parseInt(txtqty.getText());
        double dayCost= Double.parseDouble(txtDay.getText());
        double deposite = Double.parseDouble(txtDeposit.getText());
        double total = qty * deposite;
        double totalday=qty*dayCost;

        int rowIndex = isAlreadyExists(cmbserialNo.getSelectedItem().toString());

        if (rowIndex == -1) {
            Object[] rowData = {
                cmbserialNo.getSelectedItem().toString(),
                dayCost,
                qty,
                deposite,
                total,
                totalday
            };
            model.addRow(rowData);
        } else {
            qty += (int) model.getValueAt(rowIndex, 2);
            total = qty * deposite;
            totalday=qty*dayCost;
            tbIItemDetails.setValueAt(qty, rowIndex, 2);
            tbIItemDetails.setValueAt(total, rowIndex, 4);
            tbIItemDetails.setValueAt(totalday, rowIndex, 5);
        }

        calculateTotalValue();
        cmbserialNo.requestFocus();

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        String orderId = txtOrder.getText();
        String clientid = (String) cmbClientid.getSelectedItem();
        String employeid = (String) cmbEmpId.getSelectedItem();
        String orderDate = txtDate.getText();
        ArrayList<OrderDetail> orderDetailList = new ArrayList<>();
        DefaultTableModel dtm = (DefaultTableModel) tbIItemDetails.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            String serialNo = (String) dtm.getValueAt(i, 0);
           String  qty = String.valueOf(dtm.getValueAt(i, 2));
            double deposit = (double) dtm.getValueAt(i, 3);
            double day=(double) dtm.getValueAt(i, 5);
            OrderDetail orderdetail = new OrderDetail(orderId, serialNo, qty, deposit, null, day);
            orderDetailList.add(orderdetail);
        }
        Order order = new Order(orderId, clientid, employeid, orderDate, orderDetailList);
        try {
            boolean isAdded = PlaceOrderController.addOrder(order);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Added Success");
                new AdminDashBordView().setVisible(true);
                this.hide();
            } else {
                JOptionPane.showMessageDialog(this, "Added Fail");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_btnPlaceOrderActionPerformed

