    private int isAlreadyExists(String serialNo) {

        DefaultTableModel dtm = (DefaultTableModel) tbIItemDetails.getModel();

        for (int i = 0; i < dtm.getRowCount(); i++) {
            String code = (String) dtm.getValueAt(i, 0);
            if (code.equals(serialNo)) {
                return i;
            }
        }

        return -1;

    }

    private void calculateTotalValue() {

        DefaultTableModel dtm = (DefaultTableModel) tbIItemDetails.getModel();

        double total = 0;
        for (int i = 0; i < dtm.getRowCount(); i++) {
            total += (double) dtm.getValueAt(i, 4);
        }

        txtPaymnet.setText(total + "");
    }

    

