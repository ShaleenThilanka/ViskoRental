    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String serailNO = txtserial.getText();
        String brndName = cmbBrandname.getSelectedItem().toString();
        String itemname = cmbName.getSelectedItem().toString();
        String qty = txtqty.getText();
        double day = Double.parseDouble(txtDay.getText());
        double deposite = Double.parseDouble(txtDeposit.getText());

        Item item = new Item(serailNO, brndName, itemname, qty, day, deposite);
        if (!serailNO.isEmpty() && !brndName.isEmpty() && !itemname.isEmpty()  && !qty.isEmpty()) {

            try {
                boolean isAdded = ItemDetailsController.addItem(item);
                if (isAdded) {
                    JOptionPane.showMessageDialog(this, "User Added");
                    clear();
                    genarateSerialNumber();
                    loadAllItem();
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminItemDetailView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AdminItemDetailView.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Try Again? Complete All action",
                    "ERROR", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnOKActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Item item = new Item(
                txtserial.getText(),
                cmbBrandname.getSelectedItem().toString(),
                cmbName.getSelectedItem().toString(),
               
                txtqty.getText(),
                Double.parseDouble(txtDay.getText()),
               
                Double.parseDouble(txtDeposit.getText())
        );
        try {
            boolean isUpdate = ItemDetailsController.updateItem(item);
            if (isUpdate) {
                JOptionPane.showMessageDialog(this, "Update Success");
                clear();
                genarateSerialNumber();
            } else {
                JOptionPane.showMessageDialog(this, "Update fail..");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

