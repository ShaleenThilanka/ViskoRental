    private void txtserialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtserialActionPerformed
        if (txtserial.getText().trim().length() > 0) {
            try {
                Item obj = new ItemDetailsController().getItem(txtserial.getText().trim());
                if (obj != null) {
                    cmbBrandname.setSelectedItem(obj.getBrand_name());
                    cmbName.setSelectedItem(obj.getItem_name());
                    
                    
                    txtqty.setText(obj.getQty_On_Hand());
                    txtDay.setText(obj.getPer_cost_day() + "");
                    
                    txtDeposit.setText(obj.getDeposite() + "");
                } else {
                    JOptionPane.showMessageDialog(this, "NO Result Found", "Warring", JOptionPane.ERROR_MESSAGE);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Try Again? Enter Item Serail number",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtserialActionPerformed

