    private void txtnameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtnameActionPerformed
        if (txtname.getText().trim().length() > 0) {
            try {
                Employee obj = new EmployeeRegistrationController().getEmployee(txtname.getText().trim());
                if (obj != null) {
                    txtid.setText(obj.getEmployeeId());
                    txtcontact.setText(obj.getEmployeeContact());
                    txtaddress.setText(obj.getEmployeeAddress());
                } else {
                    JOptionPane.showMessageDialog(this, "NO Result Found", "Warring", JOptionPane.ERROR_MESSAGE);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Try Again? Enter Employee Name",
                    "PopUp4", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtnameActionPerformed

