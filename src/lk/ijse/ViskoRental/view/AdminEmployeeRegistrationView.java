    private void btnSarchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSarchActionPerformed
        String name = txtname.getText();
        try {
            Employee employee = EmployeeRegistrationController.sarchEmployee(name);
            if (employee != null) {
                txtid.setText(employee.getEmployeeId());
                txtcontact.setText(employee.getEmployeeContact());
                txtaddress.setText(employee.getEmployeeAddress());

            } else {
                JOptionPane.showMessageDialog(this, "User not found...");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AdminUserRegitrationView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSarchActionPerformed

