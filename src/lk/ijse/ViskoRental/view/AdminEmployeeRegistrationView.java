    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String empid = txtid.getText();
        String name = txtname.getText();
        String contact = txtcontact.getText();
        String address = txtaddress.getText();
        Employee employee = new Employee(empid,name,contact,address);
        if (!empid.isEmpty() && !name.isEmpty() && !contact.isEmpty() && !address.isEmpty()) {
            try {
                boolean isAdded = EmployeeRegistrationController.employeeAdd(employee);
                if(isAdded){
                JOptionPane.showMessageDialog(this, "Employee Added");
                clear();
                generateEmployeeId();
                loadAllEmployee();
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminEmployeeRegistrationView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } catch (SQLException ex) {
                Logger.getLogger(AdminEmployeeRegistrationView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Try Again?Complete All action",
                    "Error", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_btnAddActionPerformed

