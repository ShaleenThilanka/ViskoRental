package lk.ijse.ViskoRental.model;

import java.util.ArrayList;

/**
 *
 * @author hp
 */
public class Order {

    private String orderID;
    private String clientID;
    private String employeeId;
    private String orderDate;
    private ArrayList<OrderDetail> orderDetailList;

    public Order(String orderID, String clientID, String employeeId, String orderDate, ArrayList<OrderDetail> orderDetailList) {
        this.orderID = orderID;
        this.clientID = clientID;
        this.employeeId = employeeId;
        this.orderDate = orderDate;
        this.orderDetailList = orderDetailList;
    }

    public Order() {
    }

    

    /**
     * @return the orderID
     */
    public String getOrderID() {
        return orderID;
    }

    /**
     * @param orderID the orderID to set
     */
    public void setOrderID(String orderID) {
        this.orderID = orderID;
    }

    /**
     * @return the clientID
     */
    public String getClientID() {
        return clientID;
    }

    /**
     * @param clientID the clientID to set
     */
    public void setClientID(String clientID) {
        this.clientID = clientID;
    }

    /**
     * @return the employeeId
     */
    public String getEmployeeId() {
        return employeeId;
    }

    /**
     * @param employeeId the employeeId to set
     */
    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }

    /**
     * @return the orderDate
     */
    public String getOrderDate() {
        return orderDate;
    }

    /**
     * @param orderDate the orderDate to set
     */
    public void setOrderDate(String orderDate) {
        this.orderDate = orderDate;
    }

    /**
     * @return the orderDetailList
     */
    public ArrayList<OrderDetail> getOrderDetailList() {
        return orderDetailList;
    }

    /**
     * @param orderDetailList the orderDetailList to set
     */
    public void setOrderDetailList(ArrayList<OrderDetail> orderDetailList) {
        this.orderDetailList = orderDetailList;
    }

}
